#include "stdafx.h"
#include<iostream>

int _tmain(int argc, _TCHAR* argv[])
{
	int I, W, c,d;
	scanf_s("%d", &I);
	if (I <= 100000)
		W = I*(1 + 0.1);
	else if (I > 100000 && I <= 200000)
		W = 100000 * 0.1 + (I - 100000)*0.075+I;
	else if (I > 200000 && I <= 400000)
		W = 100000 * 0.1 + 100000 * 0.075 + (I - 200000)*0.05 + I;
	else if (I > 400000 && I <= 600000)
		W = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (I - 400000)*0.03 + I;
	else if (I > 600000 && 1000000)
		W = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + (I - 600000)*0.015 + I;
	else if (I > 1000000)
		W = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + 200000 * 0.03 + 400000 * 0.015 + (I - 1000000)*0.01 + I;
	
	printf("W=%ld\n", W);
	
	system("pause");
	return 0;
}

